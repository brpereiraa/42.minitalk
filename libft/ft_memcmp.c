#include "libft.h"

int	ft_memcmp(const void *str1, const void *str2, size_t n)
{
	unsigned char const	*ss1;
	unsigned char const	*ss2;
	size_t				i;

	i = 0;
	ss1 = (unsigned char const *)str1;
	ss2 = (unsigned char const *)str2;
	while (i < n)
	{
		if (ss1[i] != ss2[i])
			return (ss1[i] - ss2[i]);
		i++;
	}
	return (0);
}
